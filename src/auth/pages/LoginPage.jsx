import "./LoginPage.css";
import { useForm } from "../../hooks/useForm";
import { useAuthStore } from "../../hooks";

const loginFormFields = {
  loginEmail: "",
  loginPassword: "",
}

const registerFormFields = {
  registerName: "",
  registerEmail: "",
  registerPassword: "",
  registerPasswordRepeat: "",
}

export const LoginPage = () => {

  const { startLogin } = useAuthStore();
  const { loginEmail, loginPassword, onInputChange:onLoginInputChange } = useForm(loginFormFields);
  const { registerName, registerEmail, registerPassword, registerPasswordRepeat, onInputChange:onRegisterInputChange } = useForm(registerFormFields);

  const onLoginSubmit = (e) => {
    e.preventDefault();
    startLogin({email: loginEmail, password: loginPassword});
  }

  const onRegisterSubmit = (e) => {
    e.preventDefault();
    console.log({ registerName, registerEmail, registerPassword, registerPasswordRepeat });
  }

  return (
    <div className="container login-container">
      <div className="row">
        <div className="col-md-6 login-form-1">
          <h3>Ingreso</h3>
          <form onSubmit={onLoginSubmit}>
            <div className="form-group mb-2">
              <input
                type="text"
                className="form-control"
                placeholder="Correo"
                name="loginEmail"
                value={loginEmail}
                onChange={onLoginInputChange}
              />
            </div>
            <div className="form-group mb-2">
              <input
                type="password"
                className="form-control"
                placeholder="Contraseña"
                name="loginPassword"
                value={loginPassword}
                onChange={onLoginInputChange}
              />
            </div>
            <div className="form-group mb-2">
              <input type="submit" className="btnSubmit" value="Login" />
            </div>
          </form>
        </div>

        <div className="col-md-6 login-form-2">
          <h3>Registro</h3>
          <form onSubmit={onRegisterSubmit}>
            <div className="form-group mb-2">
              <input
                type="text"
                className="form-control"
                placeholder="Nombre"
                name="registerName"
                value={registerName}
                onChange={onRegisterInputChange}
              />
            </div>
            <div className="form-group mb-2">
              <input
                type="email"
                className="form-control"
                placeholder="Correo"
                name="registerEmail"
                value={registerEmail}
                onChange={onRegisterInputChange}
              />
            </div>
            <div className="form-group mb-2">
              <input
                type="password"
                className="form-control"
                placeholder="Contraseña"
                name="registerPassword"
                value={registerPassword}
                onChange={onRegisterInputChange}
              />
            </div>

            <div className="form-group mb-2">
              <input
                type="password"
                className="form-control"
                placeholder="Repita la contraseña"
                name="registerPasswordRepeat"
                value={registerPasswordRepeat}
                onChange={onRegisterInputChange}
              />
            </div>

            <div className="form-group mb-2">
              <input type="submit" className="btnSubmit" value="Crear cuenta" />
            </div>
          </form>
        </div>
      </div>
    </div>
  );
};
